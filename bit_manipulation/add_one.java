package bit_manipulation;

public class add_one {

    public static void main(String[] args) {
        int x = 3;
        System.out.println("x=" + x);
        System.out.println("x+1 is " + -~x);
        x = 8;
        System.out.println("x=" + x);
        System.out.println("x+1 is " + -~x);
    }
}
